package com.Akshay;

public class Primitives {
    public static void main(String[] args){

        // Primitive Data types: These data types cannot be broken down into sub-datatypes

        int rollNo = 727;
        char letter = 'A';
        float marks = 95.52f;
        double largeDecimalNumber = 11922992.828282;
        long largeIntegerValue = 1241929999999999994L;
        boolean flag = true;



        // String is not a primitive data type as it can be broken down into characters
        String name = "Akshay";
    }
}
