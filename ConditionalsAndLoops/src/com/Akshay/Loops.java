package com.Akshay;

public class Loops {
    public static void main(String[] args) {

        //for loop
        for(int num = 1; num <=5; num++){
            System.out.println(num);
        }

        //while loop
        int num = 1;
        while(num <= 5){
            System.out.println(num);
            num++;
        }

        //do while
        num = 1;
        do{
            System.out.println(num);
            num++;
        }while(num <= 1);
    }
}
